void test(){
    int n, m, k;
    cin >> n >> m >> k;
    // Cards got by each player
    int d = n/k;
    // which will most cards by first player
    int a1 = min(m, d);
    // cards got by the second Player
    int a2 = (m - a1 + k - 2) / (k - 1);
    // Printing the points of the winning player
    cout << (a1 - a2) << endl;
}

